				<h2>L'essor du Jiu Jitsu Brésilien</h2>

				<p>L'aîné des fils de Gastao, <strong>Carlos</strong> (1902 - 1994), eu donc l'honneur de devenir le disciple principale de <em>Maeda</em> 
				et donc de recevoir son enseignement. Bien qu'il restèrent en contact finalement que peu de temps, quelques années tout 
				au plus, du fait de l'implication importante de <em>Maeda</em> dans l'effort de colonisation nippone du Brésil, Carlos profita 
				de cet enseignement et ouvrit sa propre école en 1925.</p>

				<p>Son frère, <strong>Helio</strong>, grand perfectionniste, n'eu de cesse de chercher des techniques toujours plus efficaces et réalistes et contribua majoritairement à l'essor de l'art de la famille
				<em>Gracie</em>. Les frères <em>Gracie</em> eurent beaucoup d'enfants, qui eurent à leur tour beaucoup d'enfants, et le clan
				<em>Gracie</em> devint une dynastie dont tous les membres se passionnèrent pour cet art familial que l'on appellera logiquement
				par la suite le <strong>Gracie Jiu Jitsu</strong> et plus récemment <strong>Jiu Jitsu Brésilien</strong>. 
					
						
