				<h1>Concept</h1>

				<p>Comme nous l'avons vu plus haut, le <em>Jiu Jitsu Brésilien</em> a été élaboré par les membres de la famille <em>Gracie</em>. </p>
				
				<p>Or, les <em>Gracie</em> ont une particularité : ils sont tous dotés d'une faible corpulence physique (il est vrai que ceci tend
				   à changer au vu des gabarits des membres des nouvelles générations ...). Cette faiblesse physique, que l'on pourrait 
				   initialement prendre pour un handicap, se révêla paradoxalement être une véritable force.</p>

				 <p>En effet, au cours de leurs recherches
				   ininterrompues sur la façon de rendre leur art le plus efficace qu'il soit, les <em>Gracie</em> ont du composer avec ce facteur
				   et être encore plus rigoureux dans le choix des techniques efficaces et leur exécution; le moindre détail technique négligé 
				   engendre l'échec de la technique en cours. Bien des techniques demandant beaucoup de force ont ainsi été écartées au profit
				   de techniques plus élaborées et donc qui ne souffre d'aucune négligences mais qui, en contrepartie si elles sont parfaitement 
				   exécutées, fonctionnent inéxorablement. Et c'est ce caractère pragmatique qui fait toute la puissance du 
				   <em>Jiu Jitsu Brésilien</em>.</p>
				   
				<p>Le principe de base du <em>Jiu Jitsu Brésilien</em> est d'<strong>amener l'adversaire au sol</strong> afin de limiter au maximum l'échange de 
				coups debout.</p>
				
				<p>Pourquoi ?</p>

				<p>Et bien, comme les <em>Gracie</em> étaient de nature assez faible physiquement, s'engager dans un 
				combat de boxe, où forcément le poids est un facteur déterminant, ne serait pas forcément une bonne solution.</p>

				<p>Alors, quelle solution ont trouvé les <em>Gracie</em> pour tourner ce handicap en leur faveur ?</p>

				<p>Et bien ils en ont conclu que la meilleure chose à faire était de casser la distance entre eux et leurs adversaires afin de complètement le neutraliser. A la manière 
				d'une sangsue (le terme peut paraître désobligeant mais ce n'est qu'une simple illustration du concept!), le pratiquant
				de <em>Jiu Jitsu Brésilien</em> va se coller à son adversaire, l'emmener au sol et, tel un boa étouffant sa proie (le terme
				est déjà meilleur!), il va chercher à l'épuiser puis l'achever quand ce dernier n'aura plus la force de lutter.</p>

				<p>Ainsi, l'accent est mis sur la technique et l'endurance, des qualités propres aux personnes de plus faible corpulence physique,
				plutôt que sur la force pure. Comme diraient les pratiquants de <em>Jiu Jitsu</Em> : <em>It's a chess game</em> (<em>c'est un
				jeu d'échec</em>).</p>
				
					
