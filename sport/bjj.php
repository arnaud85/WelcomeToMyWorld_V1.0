				<h2>La consécration</h2>
				
				<p>La famille <em>Gracie</em> étant très à l'aise financièrement, ils eurent le loisir de se consacrer entièrement à la pratique
				et à l'amélioration de leur art, vouant leur vie à celui-ci. Car maintenant en autonomie complète vis à vis de <em>Maeda</em>,
				débarassés de tout dogme inutiles ou nuisibles souvent imposés par le poids des traditions, la famille <em>Gracie</em> peaufina
				son art et testa sans relâche chaque technique, éliminant au passage sans états d'âmes aucun toutes celles se révélants
				obsolètes ou inefficaces. Ainsi, le clan <em>Gracie</em> se comporte tel un laboratoire de recherche pour leur discipline
				et cette démarche pragmatique à l'extrême, que l'on pourrait même qualifier de scientifique, est à la base de leur réusssite
				phénoménale.</p>
					
				<p>Mais les <em>Gracie</em> ne s'en tiennent pas qu'à tester leurs techniques tranquillement chez eux. Encore fallait-il prouver 
				au monde entier que leur Jiu Jitsu était bel et bien efficace. En ce sens, ils ont sublimé le concept de <em>randori</em> de
				<em>Kano</em> en lançant des défis aux meilleurs artistes martiaux de leurs époques et en participant à tous les tournois 
				d'arts martiaux interdisciplinaires qui existaient.</p>
					
				<p>Parmi les personnalités les plus connues, les <em>Gracie</em> ont lancé des défis au grand judoka <strong>Kimura</strong> (une
				clé d'épaule porte encore son nom) et aux boxeurs <strong>Joe Louis</strong>, <strong>Mohammed Ali</strong> et <strong>Mike Tyson</strong>.</p> 
					
				<p>Au sommet de leur confiance, ils créent un tournoi, retransmis à la télévision américaine, réunissant les meilleurs combattants
				de la planète, toute discipline confondue (boxe anglaise, boxe française, boxe thailandaise, kick-boxing, karaté, kung-fu, 
				lutte, etc ...) et engage un de leur membre, <strong>Royce</strong>, à participer à cette compétition. <em>Royce</em> gagna cette 
				compétition haut la main (ainsi que les trois éditions suivantes) en affrontant parfois des adversaires beaucoup plus lourds
				que lui (il n'y avait pas de catégorie de poids dans cette compétition et les règles étaient pour le moins sommaires...).</p>
				
				<p>C'est à ce moment là que le Jiu Jitsu explosa aux yeux du monde entier et qu'il acquit définitivement une immense notoriété.
				Ce tournoi fut nommé <strong>Ultimate Fighting Championship</strong> (plus connu sous le nom d'<strong>UFC</strong> et devint ensuite la plus 
				grande organisation au monde de ce que l'on appelle maintenant le <strong>Mixed Martial Arts</strong> (ou <strong>MMA</strong>), <strong>Arts 
				Martiaux Mixtes</strong> en français. Cette organisation jouit aujourd'hui d'un business florissant et contribue à imposer
				le <em>MMA</em> comme un des sports les plus populaires en ce moment dans le monde entier.</p>
				
				<p>Parallèlement, un autre membre de la famille <em>Gracie</em>, <strong>Rickson</strong>, s'imposera dans un tournoi similaire,
				mais cette fois-ci au Japon, le <strong>Pride Fighting Championship</strong>, contribuant à construire la légende familiale.</p>
				
				<p>Ainsi, le <em>MMA</em> s'impose comme un sport phare du 21ème siècle et le <em>Jiu Jitsu Brésilien</em> en est à l'origine
				et en demeure un acteur majeur.</p> 
					
				<p>Au travers des générations, depuis la première avec <em>Carlos</em> et <em>Helio</em>, en passant par la deuxième avec
				<em>Carlson</em> et <em>Rolls</em>, la troisième avec (entre autres) <em>Rickson</em>, <em>Royce</em>, <em>Renzo</em> et 
				<em>Royler</em>, pour enfin arriver de nos jours avec <em>Roger</em> et ainsi de suite, le clan <em>Gracie</em> s'est forgé
				une légende battie sur des preuves concrêtes et inébranlables, de même que le Jiu Jitsu à réussi à s'imposer comme une 
				discipline terriblement efficace.</p>	
					
					
