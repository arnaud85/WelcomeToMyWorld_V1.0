				<h1>Histoire</h1>

				<h2>La genèse</h2>

				<p>A l'origine, le JJB hérite du traditionnel <strong>Ju Jutsu</strong> japonais. <em>Ju</em> signifiant <em>souplesse</em> 
				et <em>Jutsu</em> signifiant quant à lui <em>art</em> ou <em>technique</em>. Le <em>Ju Jutsu</em>, ou donc <em>art de la 
				souplesse</em>, devient donc, prononcé à la mode carioca (avec l'accent s'il vous plait^^), <em>Jiu Jitsu</em>.</p>
				
				<p>Mais à qui doit on cette technique de combat formidable ? Et bien, pour le savoir, replongeons nous quelques années en 
				arrières, c'est-à-dire vers la fin du 19ème siècle. A cette époque là, au Japon, existaient une multitude d'écoles de 
				Ju Jutsu traditionnelles dont certaines dotées d'une réputation florissante. Celles-ci enseignaient des techniques létales 
				et donc extrêmement dangeureuses. C'était à la fois la gloire de ces écoles, et en même temps, paradoxalement, une certaine
				faiblesse, puisqu'il devenait ainsi impossible de s'entrainer à pleine vitesse sans s'exposer à de graves accidents.</p>

				<p>Et c'est là qu'intervient le génie d'un homme, un certain <strong>Jigoro Kano</strong> (1860 - 1938). 
				Celui-ci décida d'éliminer toutes les techniques les plus dangeureuses du <em>Ju Jutsu</em> traditionnel 
				(piques aux yeux, coups portés dur les points vitaux et certaines clés aux articulations jugées trop dangeureuses) et introduisit 
				la notion de <em>randori</em> c'est-à-dire de combats d'entrainement.
				Ainsi, il devint possible de s'entrainer à pleine puissance sans risquer de se blesser, ce qui améliora grandement les 
				capacités en combat réel des ses pratiquants. <em>Kano</em> fonda sa propre école et la baptisa : <strong>Judo</strong> soit 
				littéralement <em>Voie de la souplesse</em> (<em>do</em> signifiant <em>voie</em> ou <em>chemin</em>). Cette nouvelle école
				connut très vite une grande popularité et chacun sait à présent la place qu'elle occupe au panthéon des sports de combat.</p>
				
					
				<p>J'en entends déjà quelques uns chuchoter au fond de la salle en se disant : "Mais quel est le rapport avec le JJB ?"</p>
				
				<p>Et bien on y vient, pas d'impatience! Donc, reprenons. <em>Kano</em> eu donc a coeur de faire connaitre son école dans 
				le monde entier et, pour ce faire, nomma deux de ses meilleurs pratiquants afin qu'ils représentent le judo à travers le monde.
				Il s'agit de : un certain <em>Tomita</em>, glorieux vétéran et fabuleux technicien, et <strong>Mitsuyo Maeda</strong> (1878 - 1941).
				<em>Maeda</em> s'occupa de lancer des défis à tous les lutteurs, boxeurs et autres pratiquants d'arts martiaux de l'époque
				afin de prouver l'efficacité de l'art créé par <em>Kano</em>. Ces défis le conduisirent aux quatre coins du monde et il acquit 
				une solide réputation. Cependant, <em>Maeda</em> fit quelques entorses au Judo lors de certains de ses défis puisqu'il lui 
				arriva d'employer des techniques de Ju Jutsu traditionnel (qu'il pratiqua lors de sa jeunesse) pour se défaire de ses 
				adversaires les plus coriaces; techniques formellement exclues du Judo car jugées trop dangeureuses. De fil en aiguille 
				<em>Maeda</em> finit par développer sa propre technique de combat et se déclara vers la fin de ses défis comme un pratiquant
				de Ju Jutsu.</p>

				<p>Au début des années 20, il débarqua au Brésil lors de la fameuse entreprise nippone de colonisation de 
				l'amérique du sud, dont il aurait eu également un rôle à jouer. Là, il rencontre un descendant de colon écossais nommé
				<strong>Gastao Gracie</strong> et les deux hommes développent rapidement une grande amitié. <em>Gracie</em> était un notable local et 
				il aida grandement <em>Maeda</em> à s'établir dans ce territoire tout neuf. Pour le remercier de son aide, <em>Maeda</em>
				entreprit d'enseigner son art de combat aux enfants de <em>Gastao Gracie</em>.</p>
				
