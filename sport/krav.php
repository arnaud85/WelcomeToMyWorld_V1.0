				<h1>Théorie et concept</h1>
					
				<p>Le <em>Krav Maga</em> repose sur un certain nombre de dogmes que nous pouvons citer ci-dessous :
				<ul>
					<li><p>Au <em>Krav Maga</em>, il n'y a <b>pas de règles</b>. Tout est bon du moment où l'on protège son intégrité physique
					et que l'on sort victorieux du combat. Ainsi, il est conseillé de récuper tous les objets à porter de main susceptibles
					de fournir une protection ou bien une arme adéquat.</p></li>
					<li><p>Les techniques ont été élaborées de façon à être <b>des prolongations naturelles des gestes instinctifs de 
					protection.</b>Ils sont donc assimilables en un minimum de temps et par tout un chacun. Comme le disait <em>Imi</em> 
					le <em>Krav Maga</em> a été construit "<em>afin que chaque personne puisse circuler en paix</em>". En outre, ils sont 
					applicables dans n'importe quelle situation, y compris de stress ultime.</p></li>
					<li><p>La réponse doit être immédiate et aussi forte que nécessaire, sous la forme d'une contre-attaque simultanément 
					effectuée avec l'attaque de l'aggresseur. Le but étant de récupérer immédiatement l'ascendant sur son adversaire 
					et de <b>ne jamais reculer</b> une fois la contre-attaque effectuée; le déplacement étant toujours fait vers l'avant.</p></li>
					<li><p>On cherchera à mettre fin au combat le plus rapidement possible de façon à être prêt à surmonter une autre aggression.
					Ainsi, <b>on priviligira les attaques sur les points vitaux</b> tels que : les parties génitales, les yeux, la gorge. 
					Il y a là clairement une recherche de l'efficacité, au détriment de l'esthétique parfois. Mais, finalement, avons-nous
					l'air plus esthétique après un combat perdu contre un adversaire motivé à nous refaire le portrait (à sa façon en plus)
					? Rien n'est moins sûr!</p></li>
					<li><p>Le <b>principe de précaution</b> : éviter de commettre des actes qui pourraient nous mettre en danger inutilement et 
					nous blesser : marcher dans des endroits mal fréquentés de notre ville, parler à des gens peu engageants ou encore mettre 
					une gifle au videur de notre boite de nuit préféré (faut pas chercher quand même!). Ce principe anodin est pour moi le plus important est devrait 
					être le dogme principal de toutes les méthodes de défense personnelle, ce qui pourrait nous éviter, à coups sûr, bien 
					des déboires.</p></li>
				</ul></p>		
					
