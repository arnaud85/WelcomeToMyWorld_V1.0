				<h2>L'exode</h2>
					
				<p>A partir de 1940, l'antisémitisme ambiant est à son apogée et <em>Imi</em> devient la cible des autorités locales, devenues 
				fascistes. Il quitte alors son pays pour la "terre promise" d'Israël en prenant le dernier navire qui réussit à échapper aux 
				nazis (un ancien bateau de rivière nommé <em>Pentcho</em> reconvertit en arche pour les réfugiés juifs d'Europe de l'Est).
				Au cours de cette traversée, <em>Imi</em> devra se jetter plusieurs fois à l'eau pour sauver des réfugiés tombés 
				malencontreusement par dessus les rembardes de sécurité. La légende raconte qu'il attrape une infection de l'oreille en voulant
				sauver un enfant de la noyade.</p>
						
				<p>Plus tard, la chaudière du <em>Pentcho</em> explose et ils échouent vers une île grecque. <em>Imi</em> et quatre de ses amis
				prennent alors une chaloupe pour tenter de trouver de l'aide en Crête mais une violente tempête fait chavirer la frêle chaloupe
				et les cinq amis n'atteignirent jamais la Crête. Ils eurent la chance qu'un navire de guerre anglais passent par là et 
				les sauvent de leur destin funeste. Il furent conduit jusqu'à Alexandrie, en Egypte. Là, <em>Imi</em> eut du fil retordre avec
				son infection qui faillit bien lui couter la vie, mais un chirurgien parvint finalement à l'opérer et donc le soigner. Après 
				avoir récupéré, notre bon <em>Imi</em> s'engagea dans la légion tchèque (alors sous les ordres britanniques) et servit pendant
				plus d'un an aux quatre coins du Moyen-Orient avant de recevoir un permis d'entrer pour Israël.</p>	
					
				<h2>Les lettres de noblesses</h2>
				
				<p>En 1942, <em>Imi</em> est présenté au général <em>Yitzhak Sadeh</em>, alors chef de la <em>Haganah</em> (signifiant "défense" 
				en Hébreu, la <em>Haganah</em> est une milice sioniste chargée de défendre les juifs de Palestine). Celui-ci l'engagera sur le 
				champ au vue de ses capacités exceptionnelles dans le combat au corps-à-corps. C'est à cette époque qu'il commence à formaliser sa 
				technique de combat au corps-à-corps, qu'il appellera tout simplement <em>Krav Maga</em> (signifiant littéralement <em>Combat 
				rapproché</em> en Hébreu). Il entraîne alors les soldats israéliens en leur apprenant sa méthode de combat; parmi ceux-ci se 
				trouvent des troupes d'élites telles que :
				<ul>
					<li>le <em>Palmach</em> (contraction de l'Hébreu <em>Plugot Maḥatz</em> signifiant <em>Troupe de choc</em>)</li>
					<li>le <em>Palyam</em> (commandos marines)</li>
				</ul>
				
				Il rédige le manuel officiel d'auto-défense de l'armée israélienne dans lequel est conditionné tout ce qu'il faut savoir pour
				se défendre face à une aggression violente : préparation physique pour le combat, dépassement des barrières psychologiques,
				le combat à mains nues, le combat au couteau, le combat à la baïonette, le combat au bâton, etc ...</p>
			 		
				<p>En 1948, avec la création officielle de l'état d'Israël et de l'<em>Israeli Defensives Forces</em> (<em>IDF</em> ou <em>Forces
				de Défense Israéliennes</em>), <em>Imi</em> devient instructeur en chef de <em>Krav Maga</em> et d'éducation physique pour 
				l'armée; poste qu'il occupera pendant plus de vingt ans, acquérant une reconnaissance énorme vis-à-vis de l'élite militaire
				israélienne.</p>
						
				<p>Ainsi, le <em>Krav Maga</em> a su s'imposer comme la méthode idéale pour répondre aux besoins de l'<em>IDF</em>. A savoir,
				cette méthode devait permettre autant aux membres des troupes d'élites qu'au plus simple employé de bureau de pouvoir se 
				défendre le plus efficacement possible en toutes situations, y compris de stress intense, et ceci en un minimum de temps
				d'apprentissage.</p>
						
				<p>Plus tard, le <em>Krav Maga</em> obtiendra la reconnaissance de l'Etat pour un enseignement aux civils.</p>
						
				<h2>La diffusion des connaissances</h2>
					
				<p>A partir de 1964, <em>Imi</em> quitte l'<em>IDF</em> (il continuera néanmoins à rester consultant et formateur sur sa méthode
				   de combat pour l'<em>IDF</em> et d'autres unités de sécurité) et se lance dans une grande entreprise de diffusion de sa méthode 
				   aux civils en adaptant celle-ci pour que tout un chacun, hommes comme femmes, jeunes comme moins jeunes, puissent l'utiliser.
				   Il établit deux camps d'entrainements, l'un à Tel Aviv, l'autre à Netanya qui est sa ville de résidence. Il adopte le système
				   de ceintures de couleur, propre aux arts martiaux traditionnels, pour un soucis de structuration de la méthode et assurer une 
				   progression rapide et sécurisée. <strong>Eli Avikzar</strong> devient la première ceinture noire de <em>Krav Maga</em> de l'histoire
				   lorsque <em>Imi</em> lui remet en personne le 5 janvier 1971.</p>
					   
				<p>En 1972, il démarre la première formation d'instructeurs destinée aux civils à l'institut <em>Wingate</em>. Ceci aura pour 
				effet de répandre massivement sa méthode de combat.</p>					
						
				<p>En 1978, il crée l'<em>Israeli Krav Maga Association</em> (<em>IKMA</em> ou <em>Association Israelienne de Krav Maga</em>) dont
				il restera le président jusqu'à la fin de sa vie. Le <em>Krav Maga</em> commence alors à se propager d'une manière fulgurante
				dans le monde entier par l'intermédiaire des disciples d'<em>Imi</em> : 
				<ul>
					<li><p><strong>Eyal Yanilov</strong> aux Etats-Unis</p></li> 
					<li><p><strong>Kobi Lichtenstein</strong> en Amérique du Sud</p></li>
					<li><p><strong>Richard Douïeb</strong> en Europe (1988)</p></li> 
				</ul>
				
				En 1984, <strong>Darren Levine</strong> et <strong>Allen Feldman</strong> deviennent les deux premiers américains à acquérir une ceinture noire
				de <em>Krav Maga</em>.
				Par la suite, le <em>Krav Maga</em> s'imposa comme une méthode de référence dans la plupart des troupes d'élites du monde 
				entier : le <em>FBI</em> (<em>Federal Bureau of Investigation</em> ou <em>Bureau d'enquêtes fédéral</em>), les <em>marines</em> 
				américains, les <em>SAS</em> britanniques (<em>Special Air Service</em> ou <em>Forces spéciales aériennes</em>, les commandos
				parachutistes) ou encore le <em>GIGN</em> (<em>Groupe d'Intervention de la Gendarmerie Nationale</em>) français.</p>
					
				<p>Jusqu'à ses derniers jours (il s'éteindra le 8 janvier 1998), <em>Imi Lichtenfeld</em> ne cessa de s'impliquer totalement dans
				le développement de sa méthode de combat, affinant les techniques et les concepts et supervisant les instructeurs. Il continua 
				à prôner son désir primordial : "<em>chaque enfant doit savoir se défendre, élevé dans le respect d'autrui</em>".</p>
				
