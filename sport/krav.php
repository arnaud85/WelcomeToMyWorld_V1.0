				<p>Existe-t-il une méthode permettant de neutraliser le plus rapidement et le plus efficacement possible une aggression
				pouvant se révêler potentiellemnt létale ?</p>
				
				<p>Oui, il en existe plusieurs d'ailleurs; il suffit d'aller regarder du côté des sports de combats et arts martiaux. Citons par exemple la boxe thailandaise, le Jiu Jitsu, etc ...</p>
				
				<p>Maintenant, posons nous la question suivante : existe-t-il une méthode permettant de neutraliser le plus rapidement et le plus 
				efficacement possible une aggression pouvant se révêler potentiellemnt létale, et qui ne nécessite pas des années de pratique
				intensives et une condition physique parfaite ?</p>

				<p>Oui, mesdames et messieurs. Cette technique est d'origine israëlienne et se nomme <strong>Krav Maga</strong>, ce que l'on peut littéralement traduire par : <strong>Combat rapproché</strong>, ou bien en des termes plus 
				militaires <strong>Close combat</strong>.</p>
			
				<h1>Histoire</h1>
					
				<h2>La genèse</h2>
					
				<p>Tout commence au début du vingtième siècle, dans la ville de Bratislava, qui, à l'époque, faisait partie de l'ancien empire
				austro-hongrois (aujourd'hui située en slovaquie). Comme toutes les villes d'Europe de l'Est, Bratislava abritait une 
				importante communauté juive ashkénaze (population juive issue en grande majorité de Pologne, de Russie et d'Allemagne
				possédant une langue qui leur est propre, le <em>yiddish</em>, dérivée de l'Allemand et qui emprunte des termes à l'Hébreu, 
				le Polonais et le Russe).</p>
				
				<p><strong>Samuel Lichtenfeld</strong> résidait alors à Bratislava et occupait la fonction de détective et instructeur en chef de la 
				police locale. Ancien acrobate et lutteur, il jouissait d'une très bonne réputation de par ses capacités à résoudre les 
				affaires les plus complexes ainsi que sa maitrise des techniques de neutralisation et d'arrestation de malfrats.</p>
						
				<p>Samuel eu un fils, <strong>Imrich</strong> (dit "<strong>Imi</strong>"), qui naquit en 1910, dans la ville de Budapest (située dans l'actuelle
				   Hongrie). Il baigna dès son plus jeune âge dans ce milieu policier et très sportif. Il développa ainsi très rapidement des 
				   qualités exceptionnelles dans les divers sports qu'il put pratiquer, notamment en lutte, boxe anglaise et gymnastique. 
				   A l'âge de 18 ans, il remporte le championnat national de la jeunesse Tchèque (aujourd'hui Slovaque) et, un an plus tard
				   à l'âge de 19 ans, remporte ce même championnat mais cette fois-ci chez les adultes et dans deux catégories de poids 
				   différentes. La même année, il remporte également le championnat national de boxe anglaise ainsi qu'une compétition 
				   internationale de gymnastique! Autant dire que ce sacré <em>Imi</em> avait de la ressource!</p>   				   
					
				<p>Jusqu'à l'âge de trente ans, <em>Imi</em> se consacrera quasiment exclusivement à la lutte, étant à la fois entraineur et 
				compétiteur, et gagnant une douzaine de récompenses dans ce domaine. Il est alors considéré comme l'un des meilleurs lutteurs
				européens. Il enseigne également la gymnastique à une célèbre troupe de théâtre tchèque et prends des cours d'art dramatiques,
				faisant quelques apparitions dans diverses productions.</p>	
					
				<p>Dans l'Europe des années 30, le fascisme commence à monter, notamment avec l'accession au pouvoir d'Hitler en 1934, et les
				conditions de vie des juifs d'Europe centrale et de l'est se détériorent rapidement, ceux-ci subissant des aggressions à
				répétition, pour la plupart mortelles. <em>Imi</em> prend alors part à de nombreux affrontements dans la rue, s'opposant aux 
				aggresseurs antisémites qui tentaient de pénétrer dans le quartier juif de Bratislava. Il constituera même une sorte de milice
				chargée de défendre les juifs de Bratislava, recrutant les jeunes juifs pratiquant des disciplines martiales telles que la
				lutte ou la boxe anglaise.</p>

				<p>C'est à ce moment là qu'<em>Imi</em> prend conscience de la différence entre les combats sportifs,
				qui, bien que parfois violents, se doivent de respecter certaines règles, et les combats de rue où tous les coups sont permis
				et où il n'est pas rare également de voir un individu seul devoir se défaire de plusieurs assaillants à la fois. Il entame
				alors une réflexion qui lui permettra de poser les bases des principes fondamentaux du <em>Krav Maga</em>. Parmi ceux-ci 
				citons:
				
				<ul>
					<li><strong>éviter d'être blessé</strong> et privilégier <strong>les gestes d'auto-défense</strong> (il prendra d'ailleurs conscience de ce principe lors 
					qu'une blessure aux côtes l'empêchera de participer à une compétition de lutte)</li>
					<li>utiliser des techniques basées sur des <strong>réflexes naturels</strong>, sur l'instinct pour optimiser la rapidité d'exécution
					et la phase d'apprentissage</li>
					<li><strong>contre-attaquer</strong> avec la technique appropriée et <strong>ne pas reculer</strong>; il n'y a pas de mouvement de protection ou d'esquive
					au <em>Krav Maga</em>, comme dans un combat de boxe par exemple, et chaque mouvement défensif doit permettre de 
					contre-attaquer simultanément.</li>
				</ul></p>
						
						 
					
